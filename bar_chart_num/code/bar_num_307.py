
import matplotlib.pyplot as plt
fig = plt.figure(figsize=(8, 4))
ax = fig.add_subplot(1, 1, 1)
ax.set_title('Average House Price and Average Rent Price from 2020 to 2023')
ax.set_ylabel('Price')
ax.set_xlabel('Year')
ax.bar(['2020', '2021', '2022', '2023'], [200, 220, 240, 260], width=0.3, label='Average House Price')
ax.bar(['2020', '2021', '2022', '2023'], [550, 570, 590, 610], width=0.3, bottom=[200, 220, 240, 260], label='Average Rent Price')
ax.legend()
ax.annotate('200', xy=('2020', 200), xytext=('2020', 205))
ax.annotate('550', xy=('2020', 550), xytext=('2020', 555))
ax.annotate('220', xy=('2021', 220), xytext=('2021', 225))
ax.annotate('570', xy=('2021', 570), xytext=('2021', 575))
ax.annotate('240', xy=('2022', 240), xytext=('2022', 245))
ax.annotate('590', xy=('2022', 590), xytext=('2022', 595))
ax.annotate('260', xy=('2023', 260), xytext=('2023', 265))
ax.annotate('610', xy=('2023', 610), xytext=('2023', 615))
plt.xticks(['2020', '2021', '2022', '2023'], ['2020', '2021', '2022', '2023'])
fig.tight_layout()
plt.savefig('Bar Chart/png/284.png')
plt.clf()