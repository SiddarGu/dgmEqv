
import matplotlib.pyplot as plt
fig=plt.figure(figsize=(10,8)) 
ax=fig.add_subplot(111) 
ax.bar(['USA','UK','Germany','France'],[500,600,450,550],label='Students',bottom=0) 
ax.bar(['USA','UK','Germany','France'],[100,110,90,120],label='Teachers',bottom=500) 
ax.set_title("Number of students and teachers in four countries in 2021")
ax.set_ylabel('Number')
ax.set_xlabel('Country') 
ax.legend(loc='upper left') 
ax.set_xticks(['USA','UK','Germany','France'])
ax.annotate('500',xy=('USA',500),xytext=(-30,30),textcoords='offset points')
ax.annotate('100',xy=('USA',100),xytext=(-30,30),textcoords='offset points')
ax.annotate('600',xy=('UK',600),xytext=(-30,30),textcoords='offset points')
ax.annotate('110',xy=('UK',110),xytext=(-30,30),textcoords='offset points')
ax.annotate('450',xy=('Germany',450),xytext=(-30,30),textcoords='offset points')
ax.annotate('90',xy=('Germany',90),xytext=(-30,30),textcoords='offset points')
ax.annotate('550',xy=('France',550),xytext=(-30,30),textcoords='offset points')
ax.annotate('120',xy=('France',120),xytext=(-30,30),textcoords='offset points')
plt.tight_layout()
plt.savefig('Bar Chart/png/252.png')
plt.clf()