ax.annotate('600', xy=('XYZ Corp', 600), xytext=(-10, 10), textcoords='offset points')
ax.annotate('1100', xy=('XYZ Corp', 1100), xytext=(-10, 10), textcoords='offset points')
ax.annotate('150', xy=('PQR Corp', 150), xytext=(-10, 10), textcoords='offset points')
ax.annotate('550', xy=('PQR Corp', 550), xytext=(-10, 10), textcoords='offset points')
ax.annotate('1200', xy=('PQR Corp', 1200), xytext=(-10, 10), textcoords='offset points')
ax.annotate('250', xy=('STU Corp', 250), xytext=(-10, 10), textcoords='offset points')
ax.annotate('450', xy=('STU Corp', 450), xytext=(-10, 10), textcoords='offset points')
ax.annotate('1300', xy=('STU Corp', 1300), xytext=(-10, 10), textcoords='offset points')
plt.tight_layout()
plt.savefig('Bar Chart/png/193.png')
