
import matplotlib.pyplot as plt

plt.figure(figsize=(10,7))
ax = plt.subplot()
ax.bar(['ABC Corp', 'XYZ Corp', 'PQR Corp', 'STU Corp'], [200, 300, 150, 250], label='Machinery', color='C1')
ax.bar(['ABC Corp', 'XYZ Corp', 'PQR Corp', 'STU Corp'], [500, 600, 550, 450], bottom=[200, 300, 150, 250], label='Raw Materials', color='C2')
ax.bar(['ABC Corp', 'XYZ Corp', 'PQR Corp', 'STU Corp'], [1000, 1100, 1200, 1300], bottom=[700, 900, 700, 700], label='Employees', color='C3')
ax.set_title('Distribution of Machinery, Raw Materials and Employees in Four Companies in 2021')
ax.set_xticks(['ABC Corp', 'XYZ Corp', 'PQR Corp', 'STU Corp'])
ax.set_xlabel('Company')
ax.set_ylabel('Amount')
ax.legend(loc='upper left')
ax.annotate('200', xy=('ABC Corp', 200), xytext=(-10, 10), textcoords='offset points')
ax.annotate('500', xy=('ABC Corp', 500), xytext=(-10, 10), textcoords='offset points')
ax.annotate('1000', xy=('ABC Corp', 1000), xytext=(-10, 10), textcoords='offset points')
