ax.bar('USA', [300,220,320], bottom=[100,450,580], width=0.2, label='Takeaway', color='g')
ax.bar('UK', [90,200,280], width=0.2, color='b')
ax.bar('UK', [200,220,320], bottom=[90,200,280], width=0.2, color='r')
ax.bar('UK', [280,210,340], bottom=[90,400,580], width=0.2, color='g')
