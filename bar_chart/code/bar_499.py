
import matplotlib.pyplot as plt
plt.figure(figsize=(10,6))
ax = plt.subplot()
ax.bar("New York",400000,bottom=0,width=0.2,label="Average Home Price")
ax.bar("New York",3000,bottom=400000,width=0.2,label="Average Rent")
ax.bar("Los Angeles",500000,bottom=0,width=0.2,label="Average Home Price")
ax.bar("Los Angeles",3500,bottom=500000,width=0.2,label="Average Rent")
ax.bar("London",600000,bottom=0,width=0.2,label="Average Home Price")
ax.bar("London",4000,bottom=600000,width=0.2,label="Average Rent")
ax.bar("Tokyo",800000,bottom=0,width=0.2,label="Average Home Price")
ax.bar("Tokyo",5000,bottom=800000,width=0.2,label="Average Rent")
plt.xticks(rotation=45,ha='right')
plt.title("Average Home Prices and Rents in Four Major Cities in 2021")
ax.set_ylabel("Price (USD)")
ax.legend(bbox_to_anchor=(1.02,1), loc="upper left")
plt.tight_layout()
plt.savefig("bar chart/png/178.png")
plt.clf()