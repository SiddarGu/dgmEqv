
import matplotlib.pyplot as plt
plt.figure(figsize=(12,8))
ax = plt.subplot()
ax.plot([2000,2001,2002,2003,2004], [50,60,70,80,90], label='Frozen Food Sales (billion dollars)', marker='o')
ax.plot([2000,2001,2002,2003,2004], [20,30,40,50,60], label='Organic Food Sales (billion dollars)', marker='o')
ax.plot([2000,2001,2002,2003,2004], [30,40,50,60,80], label='Convenience Food Sales (billion dollars)', marker='o')
plt.xlabel('Year')
plt.ylabel('Sales (billion dollars)')
plt.title('Trend of Sales in the Food and Beverage Industry from 2000 to 2004')
plt.legend(loc='center left', bbox_to_anchor=(1, 0.5))
ax.xaxis.set_ticks(range(2000, 2005, 1))
plt.xticks(rotation=45, wrap=True)
ax.text(2000, 50, '50', ha='center', va='bottom')
ax.text(2001, 60, '60', ha='center', va='bottom')
ax.text(2002, 70, '70', ha='center', va='bottom')
ax.text(2003, 80, '80', ha='center', va='bottom')
ax.text(2004, 90, '90', ha='center', va='bottom')
plt.tight_layout()
plt.savefig('line chart/png/464.png')
plt.clf()