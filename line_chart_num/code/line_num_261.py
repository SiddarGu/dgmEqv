

import matplotlib.pyplot as plt
plt.figure(figsize=(8,6))
ax = plt.subplot()
ax.plot(['2010','2011','2012','2013','2014'], [2.0,2.5,2.7,2.8,3.2], label='Passenger Traffic(billion passengers/year)')
ax.plot(['2010','2011','2012','2013','2014'], [2.2,2.6,2.9,3.2,3.5], label='Cargo Traffic(billion tons/year)')
ax.set_title("Global Aviation Traffic Growth from 2010 to 2014")
ax.set_xlabel("Year")
ax.set_ylabel("Traffic")
ax.legend()
plt.xticks(['2010','2011','2012','2013','2014'])
ax.annotate('2.0', xy = ('2010',2.0))
ax.annotate('2.2', xy = ('2010',2.2))
ax.annotate('2.5', xy = ('2011',2.5))
ax.annotate('2.6', xy = ('2011',2.6))
ax.annotate('2.7', xy = ('2012',2.7))
ax.annotate('2.9', xy = ('2012',2.9))
ax.annotate('2.8', xy = ('2013',2.8))
ax.annotate('3.2', xy = ('2013',3.2))
ax.annotate('3.2', xy = ('2014',3.2))
ax.annotate('3.5', xy = ('2014',3.5))
plt.tight_layout()
plt.savefig("line chart/png/364.png")
plt.clf()