
import matplotlib.pyplot as plt
import matplotlib.ticker as ticker

fig = plt.figure(figsize=(14, 8))
ax = fig.add_subplot(1, 1, 1)
ax.set_title('Changes in Crime, Arrests and Convictions in the United States, 2020 - 2023')
ax.set_xlabel('Year')
ax.set_ylabel('Number')
ax.plot([2020, 2021, 2022, 2023], [20000, 18000, 19000, 17000], label='Number of Crimes')
ax.plot([2020, 2021, 2022, 2023], [15000, 14000, 15000, 16000], label='Number of Arrests')
ax.plot([2020, 2021, 2022, 2023], [10000, 8000, 12000, 11000], label='Number of Convictions')
ax.legend(loc='best')  
ax.grid(which='both')
ax.xaxis.set_major_locator(ticker.MultipleLocator(1))
ax.annotate('20000', xy=(2020, 20000), xytext=(2019.8, 20400), arrowprops={'arrowstyle':'->'})
ax.annotate('18000', xy=(2021, 18000), xytext=(2020.8, 18400), arrowprops={'arrowstyle':'->'})
ax.annotate('19000', xy=(2022, 19000), xytext=(2021.8, 19400), arrowprops={'arrowstyle':'->'})
ax.annotate('17000', xy=(2023, 17000), xytext=(2022.8, 17400), arrowprops={'arrowstyle':'->'})
ax.annotate('15000', xy=(2020, 15000), xytext=(2019.8, 15400), arrowprops={'arrowstyle':'->'})
ax.annotate('14000', xy=(2021, 14000), xytext=(2020.8, 14400), arrowprops={'arrowstyle':'->'})
ax.annotate('15000', xy=(2022, 15000), xytext=(2021.8, 15400), arrowprops={'arrowstyle':'->'})
ax.annotate('16000', xy=(2023, 16000), xytext=(2022.8, 16400), arrowprops={'arrowstyle':'->'})
ax.annotate('10000', xy=(2020, 10000), xytext=(2019.8, 10400), arrowprops={'arrowstyle':'->'})
ax.annotate('8000', xy=(2021, 8000), xytext=(2020.8, 8400), arrowprops={'arrowstyle':'->'})
ax.annotate('12000', xy=(2022, 12000), xytext=(2021.8, 12400), arrowprops={'arrowstyle':'->'})
ax.annotate('11000', xy=(2023, 11000), xytext=(2022.8, 11400), arrowprops={'arrowstyle':'->'})
plt.tight_layout()
