
import matplotlib.pyplot as plt
plt.figure(figsize=(10,6))
plt.plot([2001, 2002, 2003, 2004], [10000, 12000, 8000, 15000], label='Wheat Production(tons)')
plt.plot([2001, 2002, 2003, 2004], [8000, 9000, 11000, 12000], label='Rice Production(tons)')
plt.plot([2001, 2002, 2003, 2004], [12000, 11000, 13000, 14000], label='Corn Production(tons)')
plt.plot([2001, 2002, 2003, 2004], [15000, 16000, 12000, 8000], label='Soybean Production(tons)')
plt.title("Crop Production in the U.S. from 2001 to 2004")
plt.xticks([2001, 2002, 2003, 2004])
plt.xlabel("Year")
plt.ylabel("Production(tons)")
plt.legend(loc='upper left', bbox_to_anchor=(1,1))
plt.annotate('Wheat\n10000', xy=(2001, 10000), xytext=(2002, 12000), arrowprops=dict(arrowstyle='->'))
plt.annotate('Rice\n8000', xy=(2001, 8000), xytext=(2002, 9000), arrowprops=dict(arrowstyle='->'))
plt.annotate('Corn\n12000', xy=(2001, 12000), xytext=(2002, 11000), arrowprops=dict(arrowstyle='->'))
plt.annotate('Soybean\n15000', xy=(2001, 15000), xytext=(2002, 16000), arrowprops=dict(arrowstyle='->'))
plt.tight_layout()
plt.savefig('line chart/png/105.png')
plt.clf()