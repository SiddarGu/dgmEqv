
import matplotlib.pyplot as plt

plt.figure(figsize=(10, 8))
ax = plt.subplot()
plt.plot([2010,2011,2012,2013,2014], [100,200,400,800,1500], label='User Count A')
plt.plot([2010,2011,2012,2013,2014], [200,300,500,1000,1700], label='User Count B')
plt.plot([2010,2011,2012,2013,2014], [500,700,900,1300,2000], label='User Count C')
ax.annotate('User Count A: 100', xy=(2010, 100), xytext=(2010, 110))
ax.annotate('User Count A: 200', xy=(2011, 200), xytext=(2011, 210))
ax.annotate('User Count A: 400', xy=(2012, 400), xytext=(2012, 410))
ax.annotate('User Count A: 800', xy=(2013, 800), xytext=(2013, 810))
ax.annotate('User Count A: 1500', xy=(2014, 1500), xytext=(2014, 1510))
ax.annotate('User Count B: 200', xy=(2010, 200), xytext=(2010, 210))
ax.annotate('User Count B: 300', xy=(2011, 300), xytext=(2011, 310))
ax.annotate('User Count B: 500', xy=(2012, 500), xytext=(2012, 510))
ax.annotate('User Count B: 1000', xy=(2013, 1000), xytext=(2013, 1010))
ax.annotate('User Count B: 1700', xy=(2014, 1700), xytext=(2014, 1710))
ax.annotate('User Count C: 500', xy=(2010, 500), xytext=(2010, 510))
ax.annotate('User Count C: 700', xy=(2011, 700), xytext=(2011, 710))
ax.annotate('User Count C: 900', xy=(2012, 900), xytext=(2012, 910))
ax.annotate('User Count C: 1300', xy=(2013, 1300), xytext=(2013, 1310))
ax.annotate('User Count C: 2000', xy=(2014, 2000), xytext=(2014, 2010))
plt.title('Growth of Online User Count from 2010 to 2014')
plt.xlabel('Year')
plt.ylabel('User Count')
plt.xticks([2010,2011,2012,2013,2014])
plt.legend(loc='best', fontsize=10)
plt.grid(True, linestyle='-.')
plt.tight_layout()
