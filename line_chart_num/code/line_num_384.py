
import matplotlib.pyplot as plt
fig = plt.figure(figsize=(8,5)) 
ax = fig.add_subplot()
ax.plot(['2017','2018','2019','2020'],[200,180,220,240], label='Donation A') 
ax.plot(['2017','2018','2019','2020'],[150,180,160,220], label='Donation B') 
ax.plot(['2017','2018','2019','2020'],[250,220,300,260], label='Donation C') 
ax.plot(['2017','2018','2019','2020'],[300,350,280,320], label='Donation D') 
plt.xticks(['2017','2018','2019','2020']) 
plt.title('Annual Donations to Four Nonprofit Organizations in the US') 
ax.legend(loc='upper left',bbox_to_anchor=(1,1))
plt.annotate('Donation A\n240 million dollars', xy = ('2020',240), xytext = ('2021', 250), arrowprops=dict(facecolor='black', shrink=0.05))
plt.annotate('Donation B\n220 million dollars', xy = ('2020',220), xytext = ('2021', 230), arrowprops=dict(facecolor='black', shrink=0.05))
plt.annotate('Donation C\n260 million dollars', xy = ('2020',260), xytext = ('2021', 270), arrowprops=dict(facecolor='black', shrink=0.05))
plt.annotate('Donation D\n320 million dollars', xy = ('2020',320), xytext = ('2021', 330), arrowprops=dict(facecolor='black', shrink=0.05))
plt.grid(True) 
plt.tight_layout()
