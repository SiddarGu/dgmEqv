
import matplotlib.pyplot as plt
plt.figure(figsize=(10, 6))
ax = plt.subplot()
ax.set_title('Home Prices and Number of Homes Sold in the US Housing Market from 2000-2004')
ax.set_xlabel('Year')
ax.set_ylabel('Average Home Price(thousand dollars)')
ax.plot(['2000','2001','2002','2003','2004'],[100,110,120,130,140],color='green',linewidth=2.5, label="Average Home Price")
ax.plot(['2000','2001','2002','2003','2004'],[6000,7000,8000,9000,10000],color='red',linewidth=2.5, label="Number of Homes Sold")
ax.legend(loc="upper left")
ax.annotate('6000',xy=('2000',100))
ax.annotate('7000',xy=('2001',110))
ax.annotate('8000',xy=('2002',120))
ax.annotate('9000',xy=('2003',130))
ax.annotate('10000',xy=('2004',140))
plt.xticks(['2000','2001','2002','2003','2004'])
plt.grid(True)
plt.tight_layout()
plt.savefig('line chart/png/77.png')
plt.clf()