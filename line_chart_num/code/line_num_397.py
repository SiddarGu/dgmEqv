
import matplotlib.pyplot as plt
plt.figure(figsize=(12,6))
ax=plt.subplot()
ax.set_title('Growth of Donations and Volunteers in Charity Organization from 2016 to 2020')
ax.set_xlabel('Year')
ax.set_ylabel('Amount')
ax.grid(True, color='gray', linestyle='--', linewidth=1, alpha=0.5)
ax.plot([2016,2017,2018,2019,2020], [300,450,500,600,700], color='red', linestyle='--', label='Donations')
ax.plot([2016,2017,2018,2019,2020], [5000,6000,7000,8000,9000], color='blue', linestyle='--', label='Volunteers')
ax.legend(loc='best')
ax.set_xticks([2016,2017,2018,2019,2020])
ax.annotate('300', xy=(2016, 300), xytext=(2016, 320), arrowprops=dict(facecolor='red'))
ax.annotate('450', xy=(2017, 450), xytext=(2017, 470), arrowprops=dict(facecolor='red'))
ax.annotate('500', xy=(2018, 500), xytext=(2018, 520), arrowprops=dict(facecolor='red'))
ax.annotate('600', xy=(2019, 600), xytext=(2019, 620), arrowprops=dict(facecolor='red'))
ax.annotate('700', xy=(2020, 700), xytext=(2020, 720), arrowprops=dict(facecolor='red'))
ax.annotate('5000', xy=(2016, 5000), xytext=(2016, 4800), arrowprops=dict(facecolor='blue'))
ax.annotate('6000', xy=(2017, 6000), xytext=(2017, 5800), arrowprops=dict(facecolor='blue'))
ax.annotate('7000', xy=(2018, 7000), xytext=(2018, 6800), arrowprops=dict(facecolor='blue'))
ax.annotate('8000', xy=(2019, 8000), xytext=(2019, 7800), arrowprops=dict(facecolor='blue'))
ax.annotate('9000', xy=(2020, 9000), xytext=(2020, 8800), arrowprops=dict(facecolor='blue'))
plt.tight_layout()
plt.savefig('line chart/png/294.png')
plt.clf()